#include <stdio.h>
#include "ft_ssl_md5.h"

void	rev_endian(unsigned n)
{
	ft_printf("%02x%02x%02x%02x", n & 0xff, (n >> 8) & 0xff, (n >> 16)
			& 0xff, n >> 24);
}
